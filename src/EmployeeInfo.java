    private void btn_imageUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_imageUploadActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File file = chooser.getSelectedFile();
        fileName = file.getAbsolutePath();
        txt_imagePath.setText(fileName);
        try {
            File image = new File(fileName);
            BufferedImage thumb = Thumbnails.of(image).size(250, 194).asBufferedImage();
            ByteArrayOutputStream baos = new ByteArrayOutputStream();
            ImageIO.write(thumb, ".jpeg", baos);
            InputStream is = new ByteArrayInputStream(baos.toByteArray());
            byte[] buf = new byte[1024];
            // int temp;
            for (int temp; (temp = is.read(buf)) != -1;) {
                baos.write(buf, 0, temp);
            }
            viewImage = new ImageIcon(thumb);
            jLabel11.setIcon(viewImage);
            employeeImage = baos.toByteArray();
            baos.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_imageUploadActionPerformed

