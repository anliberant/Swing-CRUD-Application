    private void table_dataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_dataMouseClicked
        int row = table_data.getSelectedRow();
        String tableClick = (table_data.getModel().getValueAt(row, 0).toString());

        try {
            String sql = "SELECT * FROM employeeinfo WHERE EmployeeID = '" + tableClick + "'";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {
                String strID = rs.getString("EmployeeID");
                txt_ID.setText(strID);
                String strName = rs.getString("Name");
                txt_name.setText(strName);
                String strGender = rs.getString("Gender");
                if ("Male".equals(strGender)) {
                    jRadioButton_male.setSelected(true);
                } else if ("Female".equals(strGender)) {
                    jRadioButton_female.setSelected(true);
                }
                String strAge = rs.getString("Age");
                txt_age.setText(strAge);
                String bloodGroup = rs.getString("BloodGroup");
                txt_bloudgroup.setText(bloodGroup);
                String strContactNo = rs.getString("ContactNo");
                txt_contact_no.setText(strContactNo);
                String strQualification = rs.getString("Qualification");
                combo_qua.setSelectedItem(strQualification);
                String strDate = rs.getString("DOJ");
                Date date = new SimpleDateFormat("yyyy-mm-dd").parse(strDate);
                jDateChooser.setDate(date);
                String strAddress = rs.getString("Address");
                TextArea_address.setText(strAddress);

                byte[] imageData = rs.getBytes("EmployeeImage");
                format = new ImageIcon(imageData);
                jLabel11.setIcon(format);
                employeeImage = imageData;

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_table_dataMouseClicked

